require 'rails/generators/migration'

class ArticleGenerator < Rails::Generators::Base
  include Rails::Generators::Migration

  def self.source_root
    @_article_source_root ||= File.expand_path("../templates", __FILE__)
  end

  def self.next_migration_number(path)
    Time.now.utc.strftime("%Y%m%d%H%M%S")
  end

  def create_model_file
    template "article.rb", "app/models/article.rb"
    template "article_content.rb", "app/models/article_content.rb"
    migration_template "create_articles.rb", "db/migrate/create_articles.rb"
  end
end
